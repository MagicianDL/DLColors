#import "DLGrandientColorController.h"
#import "UIColor+DLGradient.h"
#import "DLColorEntity.h"

#define SCREEN_WIDTH [UIScreen mainScreen].bounds.size.width


@interface DLGrandientColorController ()<UICollectionViewDelegate, UICollectionViewDataSource, UICollectionViewDelegateFlowLayout>

@property (nonatomic, strong) NSArray *colorEntity;

@end

@implementation DLGrandientColorController

static NSString * const reuseIdentifier = @"Cell";

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Register cell classes
    [self.collectionView registerClass:[UICollectionViewCell class] forCellWithReuseIdentifier:reuseIdentifier];
    
    // Do any additional setup after loading the view.
    
    _colorEntity = [[DLColorEntity dataSourceForColor] objectForKey:kDLColorsKey];
    
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

